class Solution {
    public int reverse(int x) {
        if (Math.abs(x) == 2L << 32) {
            return 0;
        }
        boolean abs = false;
        if (x < 0) {
            abs = true;
            x = Math.abs(x);
        }
        Stack stack = new Stack();
        String s = String.valueOf(x);
        for (char aByte : s.toCharArray()) {
            stack.push(aByte);
        }
        String val = "";
        while (!stack.isEmpty()) {
            char b = (char) stack.pop();
            val = val + b;
        }
        if (Long.valueOf(val).compareTo(2L << 31) >= 0) {
            return 0;
        }
        int result = Integer.parseInt(val);
        if (abs) {
            result = -result;
        }
        return result;
    }
}
